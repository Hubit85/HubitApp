// Define the props for the provider
interface LanguageProviderProps {
  children: ReactNode;
}

// Create the provider component
export const LanguageProvider: React.FC<LanguageProviderProps> = ({ children }) => {
  const [language, setLanguageState] = useState<Language>("es"); // Default to Spanish

  const setLanguage = (lang: Language) => {
    setLanguageState(lang);
  };

  const t = useCallback(
    (key: string, langOverride?: Language): string => {
      const currentLang = langOverride || language;
      // Ensure that baseTranslations[currentLang] is treated as Record<string, string>
      const translations = (baseTranslations[currentLang] || baseTranslations["es"]) as Record<string, string>;
      return translations[key] || key; // Fallback to key if translation not found, or to Spanish if lang not found
    },
    [language]
  );

  return (
    <LanguageContext.Provider value={{ language, setLanguage, t }}>
      {children}
    </LanguageContext.Provider>
  );
};

// Custom hook to use the language context
export const useLanguage = () => {
  const context = useContext(LanguageContext);
  if (context === undefined) {
    throw new Error("useLanguage must be used within a LanguageProvider");
  }
  return context;
};
